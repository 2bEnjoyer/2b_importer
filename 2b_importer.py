def string_replace_x(match):
    global x_offset
    global x_loc
    x_value = int(match.group(1))
    if x_offset is None:
        x_offset = x_value - x_loc - CENTER_OF_TWOB_X_OFFSET
    x_value -= x_offset
    return f"x\": {x_value}"

def string_replace_y(match):
    global y_offset
    global y_loc
    y_value = int(match.group(1))
    if y_offset is None:
        y_offset = y_value - y_loc - CENTER_OF_TWOB_Y_OFFSET
    y_value -= y_offset
    return f"y\": {y_value}"

